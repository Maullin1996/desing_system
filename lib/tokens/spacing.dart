/// A class containing pre-defined spacing values for UI elements.
class DesignSpacing {
  DesignSpacing._(); // Private constructor to prevent instantiation.

  /// The extra extra small spacing (XXS) - 5 pixels.
  static const double spaceXXS = 5;

  /// The extra small spacing (XS) - 8 pixels.
  static const double spaceXS = 8;

  /// The small spacing (SM) - 11 pixels.
  static const double spaceSM = 11;

  /// The small-large spacing (SL) - 15 pixels.
  static const double spaceSL = 15;

  /// The medium spacing (MD) - 18 pixels.
  static const double spaceMD = 18;
  
  /// The medium spacing (MD) - 21 pixels.
  static const double spaceMDL = 21;

  /// The large spacing (LG) - 30 pixels.
  static const double spaceLG = 30;

  /// The extra large spacing (XL) - 52 pixels.
  static const double spaceXL = 52;

  /// The extra extra large spacing (XXL) - 85 pixels.
  static const double spaceXXL = 85;
}
